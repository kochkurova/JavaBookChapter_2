public class CharArithDemo {
    public static void main (String args []) {




        char ch;
        ch = 'x';
        System.out.println("ch это символ " + ch);

        ch ++;
        System.out.println("теперь ch это " + ch);

        ch = 90;
        System.out.println("теперь ch это " + ch);

        ch = 7;
        System.out.println("теперь cr это " + ch);




    }
}
